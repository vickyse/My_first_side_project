class rebar:
    """
        rebar_number_and_price.put(3, 1.0);
    """

    def __init__(self) -> None:
        """初始化"""
        self.number = 0  # 編號
        self.depth = 0
        self.price_per_cm = 0  # 植一公分的價格
        self.amount_planted = 0  # 植了幾支

    def set_number(self, the_number: int) -> None:
        """設置鋼筋的編號"""
        self.number = the_number

    def get_number(self) -> int:
        """返回鋼筋的編號"""
        return self.number

    def set_depth(self, the_depth: int) -> None:
        """設置鋼筋的深度"""
        self.depth = the_depth

    def get_depth(self) -> int:
        """返回鋼筋的深度"""
        return self.depth

    def set_price_per_cm(self) -> None:
        """依照編號決定植一公分的此種鋼筋要多少錢的"""
        number_price = {3: 1, 4: 1, 5: 1.36, 6: 2.235, 7: 3.578, 8: 4.095, 10: 3.833}
        # 4號鋼筋原為1公分0.909元
        # 5號鋼筋原為1公分1.2元
        # 6號鋼筋原為1公分2.059元
        # 7號鋼筋原為1公分2.858元
        # 8號鋼筋原為1公分3.334元
        # 10號鋼筋元為1公分x元
        self.price_per_cm = number_price.get(self.number, 0)

    def get_price_per_cm(self) -> int:
        """依照set的結果返回價格"""
        return self.price_per_cm

    def get_revenue(self) -> int:
        """返回植入此種鋼筋所賺取的收入"""
        return self.price_per_cm * self.depth * self.amount_planted


